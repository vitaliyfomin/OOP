public class Animal {
    public void toGo() {
        System.out.println("Животное передвигается");
    }

    public void fly() {
        System.out.println("Животное не умеет летать");
    }

    public void swim() {
        System.out.println("Животное не умеет плавать");
    }

    // Новые методы
    public void run() {
        System.out.println("Животное бегает");
    }

    public void quack() {
        System.out.println("Животное не может крякать");
    }

    public void dive() {
        System.out.println("Животное не может нырять");
    }
}
